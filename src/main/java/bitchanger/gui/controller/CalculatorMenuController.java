	// TODO JavaDoc
	protected MenuItem showBitOperations;
	protected MenuItem bitOperationSymbols;
	
	
	
//	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##	##
